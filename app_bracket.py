@app.route('/stats/<token>')
def stats(token):
    if token != STATS_TOKEN:
        return 'Not found', 404

    # Build day → {round, matchup_count} and round → sorted days from bracket.json
    day_info = {}
    for m in _bracket['matchups']:
        d, r = m['day'], m['round']
        if d not in day_info:
            day_info[d] = {'round': r, 'matchup_count': 0}
        day_info[d]['matchup_count'] += 1

    round_days = {}
    for d, info in day_info.items():
        round_days.setdefault(info['round'], []).append(d)
    for r in round_days:
        round_days[r].sort()

    conn = psycopg2.connect(DATABASE_URL)
    try:
        cur = conn.cursor()

        # Votes per day: total rows and distinct ballots
        cur.execute('''
            SELECT day, COUNT(*) AS total_votes, COUNT(DISTINCT ballot_id) AS ballots
            FROM bracket_votes
            GROUP BY day
            ORDER BY day
        ''')
        day_rows = {d: {'votes': v, 'ballots': b} for d, v, b in cur.fetchall()}

        # Last 24h hourly ballots (ET)
        cur.execute('''
            SELECT
                date_trunc(\'hour\', timestamp::timestamptz AT TIME ZONE \'America/New_York\') AS hour,
                COUNT(DISTINCT ballot_id) AS ballots
            FROM bracket_votes
            WHERE timestamp::timestamptz >= NOW() - INTERVAL \'24 hours\'
            GROUP BY 1
            ORDER BY 1
        ''')
        hourly_rows = cur.fetchall()
    finally:
        conn.close()

    # Build bracket rounds table
    bracket_table = ''
    for d in sorted(day_info.keys()):
        r = day_info[d]['round']
        days_in_round = round_days[r]
        if len(days_in_round) > 1:
            day_within = days_in_round.index(d) + 1
            label = f'{ROUND_LABELS.get(r, f"Round {r}")} \u2014 Day {day_within}'
        else:
            label = ROUND_LABELS.get(r, f'Round {r}')
        data = day_rows.get(d, {'votes': 0, 'ballots': 0})
        bracket_table += (
            f'<tr><td>{label}</td>'
            f'<td style="text-align:right">{data["ballots"]:,}</td>'
            f'<td style="text-align:right">{data["votes"]:,}</td></tr>\n'
        )

    total_ballots = sum(v['ballots'] for v in day_rows.values())
    total_votes   = sum(v['votes']   for v in day_rows.values())

    # Hourly table
    hourly_table = ''
    for hour, ballots in hourly_rows:
        hourly_table += (
            f'<tr><td>{str(hour)[:16]}</td>'
            f'<td style="text-align:right">{ballots:,}</td></tr>\n'
        )

    html = f'''<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="UTF-8">
<meta name="viewport" content="width=device-width, initial-scale=1">
<title>Bracket Stats</title>
<style>
body {{ background:#1a1a1a; color:#ddd; font-family:sans-serif; padding:20px; max-width:520px; margin:0 auto; }}
h2 {{ color:#c9a84c; margin:24px 0 8px; }}
table {{ width:100%; border-collapse:collapse; font-size:0.9rem; }}
th {{ text-align:left; color:#888; border-bottom:1px solid #444; padding:4px 8px; }}
td {{ padding:4px 8px; border-bottom:1px solid #2a2a2a; }}
.total {{ color:#c9a84c; font-weight:bold; margin:6px 0 20px; }}
</style>
</head>
<body>
<h2>Top 64 Bracket</h2>
<table>
<tr><th>Round</th><th style="text-align:right">Ballots</th><th style="text-align:right">Total Votes</th></tr>
{bracket_table}
</table>
<p class="total">Total: {total_ballots:,} ballots &nbsp;/&nbsp; {total_votes:,} individual votes</p>

<h2>Last 24 hours (ballots/hour, ET)</h2>
<table>
<tr><th>Hour</th><th style="text-align:right">Ballots</th></tr>
{hourly_table}
</table>
</body>
</html>'''
    return html


