ROUND_LABELS = {1: 'Round of 64', 2: 'Round of 32', 3: 'Round of 16',
                4: 'Quarterfinals', 5: 'Semifinals', 6: 'Final'}


@app.route('/')
def index():
    _ensure_session()
    log_page_view('bracket_vote')
    ip  = _client_ip()
    day = _get_current_day()

    if _has_voted(ip, day):
        return render_template('bracket_vote.html', voted=True, day=day)

    results    = _get_results()
    day_matchups = [m for m in _bracket['matchups'] if m['day'] == day]
    if not day_matchups:
        return render_template('bracket_vote.html', voted=False, matchups=[],
                               day=day, round_label='', no_matchups=True)

    round_num   = day_matchups[0]['round']
    round_label = ROUND_LABELS.get(round_num, f'Round {round_num}')

    # Per-user shuffle: matchup order + left/right card orientation
    sid = session.get('session_id', '')
    rng = random.Random(f'{sid}-{day}')

    matchups_ctx = [
        _build_matchup_context(m, results, flip=rng.random() < 0.5)
        for m in day_matchups
    ]

    return render_template('bracket_vote.html', voted=False,
                           matchups=matchups_ctx, day=day,
                           round_label=round_label, no_matchups=False)


@app.route('/api/bracket_submit', methods=['POST'])
def api_bracket_submit():
    _ensure_session()
    ip     = _client_ip()
    device = _detect_device(request.headers.get('User-Agent', ''))
    day    = _get_current_day()

    if _has_voted(ip, day):
        return jsonify({'error': 'already_voted'}), 409

    data      = request.get_json(silent=True) or {}
    votes_raw = data.get('votes', [])   # [{matchup_id, chosen}, ...]

    # Validate: only accept matchup IDs scheduled for today
    day_matchup_ids = {m['id'] for m in _bracket['matchups'] if m['day'] == day}
    results   = _get_results()
    ballot_id = str(uuid.uuid4())
    valid_votes = []
    for v in votes_raw:
        mid = v.get('matchup_id')
        if not isinstance(mid, int) or mid not in day_matchup_ids:
            continue
        chosen = v.get('chosen') or None
        if not chosen:
            continue
        m = _matchup_by_id[mid]
        card_a, _ = _resolve_card(m, 'a', results)
        card_b, _ = _resolve_card(m, 'b', results)
        valid_votes.append((mid, chosen, card_a or '', card_b or ''))

    if not valid_votes:
        return jsonify({'error': 'no_valid_votes'}), 400

    round_ = _matchup_by_id[valid_votes[0][0]]['round']
    _log_votes(ip, day, round_, valid_votes, device, ballot_id)

    return jsonify({'ok': True, 'votes_recorded': len(valid_votes)})


