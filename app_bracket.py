def _init_pool():
    global _pool
    if not DATABASE_URL:
        raise RuntimeError('DATABASE_URL environment variable is not set.')
    _pool = ThreadedConnectionPool(minconn=1, maxconn=10, dsn=DATABASE_URL)


def init_db():
    _init_pool()
    with _get_db() as conn:
        cur = conn.cursor()

        # One row per individual vote (one IP may cast multiple matchup votes per round)
        cur.execute('''
            CREATE TABLE IF NOT EXISTS bracket_votes (
                id         SERIAL  PRIMARY KEY,
                ballot_id  TEXT    NOT NULL,
                timestamp  TEXT    NOT NULL,
                ip_address TEXT    NOT NULL,
                round      INTEGER NOT NULL,
                day        INTEGER NOT NULL,
                matchup_id INTEGER NOT NULL,
                card_a     TEXT    NOT NULL,
                card_b     TEXT    NOT NULL,
                chosen     TEXT,
                device     TEXT
            )
        ''')
        # Migrate existing tables: add columns added after initial deploy
        for col, defn in [
            ('ballot_id', "TEXT NOT NULL DEFAULT ''"),
            ('card_a',    "TEXT NOT NULL DEFAULT ''"),
            ('card_b',    "TEXT NOT NULL DEFAULT ''"),
        ]:
            cur.execute(f'ALTER TABLE bracket_votes ADD COLUMN IF NOT EXISTS {col} {defn}')
        cur.execute('ALTER TABLE bracket_votes ALTER COLUMN chosen DROP NOT NULL')

        # Final tallies written by advance_bracket.py after voting closes
        cur.execute('''
            CREATE TABLE IF NOT EXISTS bracket_results (
                matchup_id    INTEGER PRIMARY KEY,
                round         INTEGER NOT NULL,
                day           INTEGER NOT NULL,
                bracket_date  TEXT,
                card_a        TEXT    NOT NULL,
                card_b        TEXT    NOT NULL,
                seed_a        INTEGER,
                seed_b        INTEGER,
                votes_a       INTEGER NOT NULL DEFAULT 0,
                votes_b       INTEGER NOT NULL DEFAULT 0,
                winner        TEXT    NOT NULL,
                winner_seed   INTEGER,
                closed_at     TEXT    NOT NULL
            )
        ''')
        cur.execute('ALTER TABLE bracket_results ADD COLUMN IF NOT EXISTS bracket_date TEXT')

        # Personal favorites submitted alongside ballots
        cur.execute('''
            CREATE TABLE IF NOT EXISTS finals_favorite_cards (
                id            SERIAL  PRIMARY KEY,
                timestamp     TEXT    NOT NULL,
                ip_address    TEXT    NOT NULL,
                ballot_id     TEXT,
                card_a        TEXT,
                card_b        TEXT,
                card_c        TEXT,
                response_text TEXT,
                device        TEXT
            )
        ''')

        # Single-row state table
        cur.execute('''
            CREATE TABLE IF NOT EXISTS bracket_state (
                id          INTEGER PRIMARY KEY DEFAULT 1,
                current_day INTEGER NOT NULL DEFAULT 1
            )
        ''')
        cur.execute('''
            INSERT INTO bracket_state (id, current_day)
            VALUES (1, 1)
            ON CONFLICT DO NOTHING
        ''')


def _get_current_day():
    with _get_db() as conn:
        cur = conn.cursor()
        cur.execute('SELECT current_day FROM bracket_state WHERE id = 1')
        row = cur.fetchone()
        return row[0] if row else 1


def _has_voted(ip, day):
    with _get_db() as conn:
        cur = conn.cursor()
        cur.execute(
            'SELECT 1 FROM bracket_votes WHERE ip_address = %s AND day = %s LIMIT 1',
            (ip, day),
        )
        return cur.fetchone() is not None


def _get_user_favorites(ip, day):
    """Return the favorites row for this IP's ballot on the given day, or None."""
    with _get_db() as conn:
        cur = conn.cursor()
        cur.execute(
            '''SELECT f.card_a, f.card_b, f.card_c, f.response_text
               FROM finals_favorite_cards f
               JOIN bracket_votes bv ON bv.ballot_id = f.ballot_id
               WHERE bv.ip_address = %s AND bv.day = %s
               LIMIT 1''',
            (ip, day),
        )
        row = cur.fetchone()
    if not row:
        return None
    return {'card_a': row[0], 'card_b': row[1], 'card_c': row[2], 'response_text': row[3]}


def _get_user_ballot(ip, day):
    """Return the user's ballot for the given day as a list of dicts, ordered by matchup_id.

    Each dict: {matchup_id, card_a, card_b, chosen}
    chosen is None for matchups the user skipped (partial ballot).
    """
    with _get_db() as conn:
        cur = conn.cursor()
        cur.execute(
            'SELECT matchup_id, card_a, card_b, chosen '
            'FROM bracket_votes WHERE ip_address = %s AND day = %s',
            (ip, day),
        )
        rows = cur.fetchall()
    voted = {mid: {'card_a': ca, 'card_b': cb, 'chosen': ch} for mid, ca, cb, ch in rows}

    day_matchups    = sorted([m for m in _bracket['matchups'] if m['day'] == day], key=lambda x: x['id'])
    results_by_id   = _get_results()

    ballot = []
    for m in day_matchups:
        mid = m['id']
        if mid in voted:
            ballot.append({
                'matchup_id': mid,
                'card_a':     voted[mid]['card_a'],
                'card_b':     voted[mid]['card_b'],
                'chosen':     voted[mid]['chosen'],
            })
        else:
            # Skipped matchup — resolve card names from bracket structure
            name_a, _ = _resolve_card(m, 'a', results_by_id)
            name_b, _ = _resolve_card(m, 'b', results_by_id)
            ballot.append({
                'matchup_id': mid,
                'card_a':     name_a or '',
                'card_b':     name_b or '',
                'chosen':     None,
            })

    return ballot


def _log_votes(ip, day, round_, vote_pairs, device, ballot_id):
    """vote_pairs: list of (matchup_id, chosen_name, card_a, card_b)"""
    ts = datetime.now(timezone.utc).isoformat()
    with _get_db() as conn:
        cur = conn.cursor()
        for mid, chosen, card_a, card_b in vote_pairs:
            cur.execute(
                'INSERT INTO bracket_votes '
                '(ballot_id, timestamp, ip_address, round, day, matchup_id, card_a, card_b, chosen, device) '
                'VALUES (%s, %s, %s, %s, %s, %s, %s, %s, %s, %s)',
                (ballot_id, ts, ip, round_, day, mid, card_a, card_b, chosen, device),
            )


def _get_results():
    with _get_db() as conn:
        cur = conn.cursor()
        cur.execute(
            'SELECT matchup_id, card_a, card_b, seed_a, seed_b, '
            '       votes_a, votes_b, winner, winner_seed '
            'FROM bracket_results'
        )
        rows = cur.fetchall()
    return {
        mid: {
            'card_a': ca, 'card_b': cb, 'seed_a': sa, 'seed_b': sb,
            'votes_a': va, 'votes_b': vb, 'winner': w, 'winner_seed': ws,
        }
        for mid, ca, cb, sa, sb, va, vb, w, ws in rows
    }


def _client_ip():
    # CF-Connecting-IP is set by Cloudflare to the true client IP and cannot be spoofed via XFF
    cf = request.headers.get('CF-Connecting-IP', '').strip()
    if cf:
        return cf
    xff = request.headers.get('X-Forwarded-For', '')
    return xff.split(',')[0].strip() if xff else request.remote_addr


def _detect_device(ua):
    ua = (ua or '').lower()
    return 'mobile' if any(t in ua for t in ('mobile', 'android', 'iphone', 'ipad')) else 'desktop'


def log_page_view(page):
    try:
        ts  = datetime.now(timezone.utc).isoformat()
        ip  = _client_ip()
        sid = session.get('session_id')
        dev = _detect_device(request.headers.get('User-Agent', ''))
        with _get_db() as conn:
            cur = conn.cursor()
            cur.execute(
                'INSERT INTO page_views (timestamp, page, ip_address, session_id, device) '
                'VALUES (%s, %s, %s, %s, %s)',
                (ts, page, ip, sid, dev),
            )
    except Exception:
        pass


