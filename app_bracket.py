# ── Matchup resolution ─────────────────────────────────────────────────────────

def _resolve_card(matchup, side, results_by_id):
    """Return (name, seed) for 'a' or 'b' side, walking feeder chain for R2+ matchups."""
    if matchup['round'] == 1:
        return matchup[f'name_{side}'], matchup[f'seed_{side}']
    feeder_id = matchup[f'feeder_{side}']
    res = results_by_id.get(feeder_id)
    if res:
        return res['winner'], res['winner_seed']
    return None, None


def _build_matchup_context(matchup, results_by_id, flip):
    """
    Return a template-ready dict for one matchup.
    flip=True swaps which card appears on the left vs right.
    Seeds are intentionally omitted from the returned dict.
    """
    name_a, _ = _resolve_card(matchup, 'a', results_by_id)
    name_b, _ = _resolve_card(matchup, 'b', results_by_id)

    info_a = _get_card_info(name_a) if name_a else {}
    info_b = _get_card_info(name_b) if name_b else {}

    if flip:
        left_name,  right_name  = name_b,  name_a
        left_info,  right_info  = info_b,  info_a
    else:
        left_name,  right_name  = name_a,  name_b
        left_info,  right_info  = info_a,  info_b

    return {
        'id':            matchup['id'],
        'round':         matchup['round'],
        'left_name':     left_name,
        'left_img_front':left_info.get('img_front'),
        'left_img_back': left_info.get('img_back'),
        'left_sideways': left_info.get('is_sideways', False),
        'left_set':      left_info.get('set_name', ''),
        'left_year':     left_info.get('year', ''),
        'right_name':    right_name,
        'right_img_front':right_info.get('img_front'),
        'right_img_back': right_info.get('img_back'),
        'right_sideways': right_info.get('is_sideways', False),
        'right_set':      right_info.get('set_name', ''),
        'right_year':     right_info.get('year', ''),
    }


# ── Bracket page display order ─────────────────────────────────────────────────

def _shuffle_bracket_tree(r1_matchups, rng):
    """
    Recursively shuffle the bracket display order by randomly swapping subtrees
    at each level.  This preserves the tree structure: consecutive pairs in the
    output still correspond to the same R2 matchup, consecutive pairs-of-pairs
    to the same R3 matchup, etc.

    r1_matchups: list of R1 matchup dicts in natural bracket order.
    Returns: flat list of R1 matchup IDs in shuffled display order.
    """
    if len(r1_matchups) == 1:
        return [r1_matchups[0]['id']]

    if len(r1_matchups) == 2:
        a, b = r1_matchups[0]['id'], r1_matchups[1]['id']
        return [b, a] if rng.random() < 0.5 else [a, b]

    mid   = len(r1_matchups) // 2
    left  = _shuffle_bracket_tree(r1_matchups[:mid], rng)
    right = _shuffle_bracket_tree(r1_matchups[mid:], rng)
    return (right + left) if rng.random() < 0.5 else (left + right)


def _build_bracket_display(results_by_id):
    """
    Return a list of rounds, each round being an ordered list of enriched matchup dicts
    ready for the bracket page template.  Uses the pre-computed display order and flips.
    """
    # Build feeder-set → matchup lookup for R2+ matchups
    feeder_lookup = {}
    for m in _bracket['matchups']:
        if m['round'] > 1:
            feeder_lookup[frozenset({m['feeder_a'], m['feeder_b']})] = m

    # Derive ordered matchup lists for each round from the shuffled R1 order
    r1_by_id = {m['id']: m for m in _bracket['matchups'] if m['round'] == 1}
    ordered_by_round = [[r1_by_id[mid] for mid in _display_r1_order]]
    top_feeder_by_matchup_id = {}  # matchup_id → feeder_id displayed on top

    prev = ordered_by_round[0]
    for _ in range(2, _bracket['num_rounds'] + 1):
        cur_round = []
        for i in range(0, len(prev), 2):
            key = frozenset({prev[i]['id'], prev[i + 1]['id']})
            m   = feeder_lookup.get(key)
            if m:
                cur_round.append(m)
                top_feeder_by_matchup_id[m['id']] = prev[i]['id']
        ordered_by_round.append(cur_round)
        prev = cur_round

    # Enrich each matchup for display
    rounds_out = []
    for round_matchups in ordered_by_round:
        enriched = []
        for m in round_matchups:
            name_a, seed_a = _resolve_card(m, 'a', results_by_id)
            name_b, seed_b = _resolve_card(m, 'b', results_by_id)

            if m['round'] == 1:
                flip = _display_flips.get(m['id'], False)
            else:
                # Winner of the top-displayed feeder goes on top
                top_feeder_id = top_feeder_by_matchup_id.get(m['id'])
                flip = (m.get('feeder_b') == top_feeder_id)
            result = results_by_id.get(m['id'])

            if flip:
                left_name,  right_name  = name_b,  name_a
                left_seed,  right_seed  = seed_b,  seed_a
            else:
                left_name,  right_name  = name_a,  name_b
                left_seed,  right_seed  = seed_a,  seed_b

            winner      = result['winner'] if result else None
            left_wins   = (winner == left_name)  if winner else None
            right_wins  = (winner == right_name) if winner else None

            pct_left = pct_right = None
            if result:
                total = result['votes_a'] + result['votes_b']
                if total:
                    va = result['votes_b' if flip else 'votes_a']
                    vb = result['votes_a' if flip else 'votes_b']
                    pct_left  = round(100 * va / total, 1)
                    pct_right = round(100 * vb / total, 1)

            left_info  = _get_card_info(left_name)  if left_name  else {}
            right_info = _get_card_info(right_name) if right_name else {}

            enriched.append({
                'id':             m['id'],
                'round':          m['round'],
                'left_name':      left_name  or 'TBD',
                'left_seed':      left_seed,
                'left_img_front': left_info.get('img_front'),
                'left_img_back':  left_info.get('img_back'),
                'right_name':     right_name or 'TBD',
                'right_seed':     right_seed,
                'right_img_front':right_info.get('img_front'),
                'right_img_back': right_info.get('img_back'),
                'winner':         winner,
                'left_wins':      left_wins,
                'right_wins':     right_wins,
                'pct_left':       pct_left,
                'pct_right':      pct_right,
            })
        rounds_out.append(enriched)

    return rounds_out


